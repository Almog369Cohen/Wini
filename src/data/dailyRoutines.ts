/**
 * Simple deterministic hash based on string input.
 * Used to make selections consistent for a given day but varied across days.
 */
function hashString(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32-bit integer
  }
  return Math.abs(hash);
}

/**
 * Seeded pseudo-random number generator (mulberry32).
 */
function seededRandom(seed: number): () => number {
  return function () {
    seed |= 0;
    seed = (seed + 0x6d2b79f5) | 0;
    let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

/**
 * Shuffle array in place using Fisher-Yates with a seeded RNG.
 */
function seededShuffle<T>(arr: T[], rng: () => number): T[] {
  const result = [...arr];
  for (let i = result.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));
    [result[i], result[j]] = [result[j], result[i]];
  }
  return result;
}

/**
 * Returns 20 daily routine suggestions based on mood and energy level.
 * Results are deterministic for the same day (uses date as seed)
 * but vary from day to day.
 */
export function getRoutineSuggestions(mood: MoodType, energy: number): DailyRoutineSuggestion[] {
  const today = new Date().toISOString().split('T')[0]; // YYYY-MM-DD
  const seed = hashString(`${today}-${mood}-${energy}`);
  const rng = seededRandom(seed);

  const clampedEnergy = Math.max(1, Math.min(5, Math.round(energy)));

  // Determine difficulty distribution based on energy
  let difficultyWeights: Record<'easy' | 'medium' | 'hard', number>;
  if (clampedEnergy <= 2) {
    difficultyWeights = { easy: 14, medium: 5, hard: 1 };
  } else if (clampedEnergy === 3) {
    difficultyWeights = { easy: 8, medium: 8, hard: 4 };
  } else {
    difficultyWeights = { easy: 4, medium: 8, hard: 8 };
  }

  // Filter activities that match mood and energy range
  const matchingActivities = activities.filter(
    (a) => a.moods.includes(mood) && clampedEnergy >= a.minEnergy && clampedEnergy <= a.maxEnergy
  );

  // If not enough matching activities, also include neutral-mood activities
  const fallbackActivities = activities.filter(
    (a) =>
      !a.moods.includes(mood) &&
      clampedEnergy >= a.minEnergy &&
      clampedEnergy <= a.maxEnergy
  );

  // Group by difficulty
  const byDifficulty: Record<'easy' | 'medium' | 'hard', ActivityTemplate[]> = {
    easy: [],
    medium: [],
    hard: [],
  };

  for (const a of matchingActivities) {
    byDifficulty[a.difficulty].push(a);
  }

  // Shuffle each group
  byDifficulty.easy = seededShuffle(byDifficulty.easy, rng);
  byDifficulty.medium = seededShuffle(byDifficulty.medium, rng);
  byDifficulty.hard = seededShuffle(byDifficulty.hard, rng);

  // Select according to weights
  const selected: ActivityTemplate[] = [];
  const usedTitles = new Set<string>();

  function pickFrom(pool: ActivityTemplate[], count: number) {
    let picked = 0;
    for (const item of pool) {
      if (picked >= count) break;
      if (!usedTitles.has(item.title)) {
        selected.push(item);
        usedTitles.add(item.title);
        picked++;
      }
    }
    return picked;
  }

  const easyNeeded = difficultyWeights.easy;
  const medNeeded = difficultyWeights.medium;
  const hardNeeded = difficultyWeights.hard;

  let easyPicked = pickFrom(byDifficulty.easy, easyNeeded);
  let medPicked = pickFrom(byDifficulty.medium, medNeeded);
  let hardPicked = pickFrom(byDifficulty.hard, hardNeeded);

  // Fill remaining slots from other difficulties or fallback
  const totalNeeded = 20;
  let remaining = totalNeeded - selected.length;

  if (remaining > 0) {
    // Try to fill from other difficulty pools
    const easyRemaining = byDifficulty.easy.filter((a) => !usedTitles.has(a.title));
    const medRemaining = byDifficulty.medium.filter((a) => !usedTitles.has(a.title));
    const hardRemaining = byDifficulty.hard.filter((a) => !usedTitles.has(a.title));
    const allRemaining = [...easyRemaining, ...medRemaining, ...hardRemaining];
    const shuffledRemaining = seededShuffle(allRemaining, rng);
    pickFrom(shuffledRemaining, remaining);
  }

  remaining = totalNeeded - selected.length;
  if (remaining > 0) {
    // Use fallback activities
    const fallbackByDifficulty = seededShuffle(
      fallbackActivities.filter((a) => !usedTitles.has(a.title)),
      rng
    );
    pickFrom(fallbackByDifficulty, remaining);
  }

  // Ensure category diversity - shuffle the final selection
  const finalSelection = seededShuffle(selected, rng).slice(0, 20);

  // Convert to DailyRoutineSuggestion format
  return finalSelection.map((activity, index): DailyRoutineSuggestion => ({
    id: `routine-${today}-${mood}-${clampedEnergy}-${index}`,
    title: activity.title,
    description: activity.description,
    duration: activity.duration,
    difficulty: activity.difficulty,
    icon: activity.icon,
    category: activity.category,
  }));
}
